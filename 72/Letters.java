import java.util.Scanner;

public class Letters {
    public static void main(String[]args){
        Scanner sc = new Scanner(System.in);
       
        System.out.println("What is your message?");
        String str = sc.nextLine();
        
        System.out.println("Your message is " + str.length() + " characters long.");
        System.out.println("The first character is at position 0 and is '" + str.charAt(0) +"'.");
        System.out.println("The last character is at position " + str.length() + " and is '" + str.charAt(str.length()- 1) +"'.");
        
    }
} 
